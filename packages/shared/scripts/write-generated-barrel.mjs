import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const generatedRoot = path.join(__dirname, '..', 'generated');

function collectDtsFiles(dir, base = dir, acc = []) {
  if (!fs.existsSync(dir)) return acc;
  for (const name of fs.readdirSync(dir)) {
    const full = path.join(dir, name);
    const st = fs.statSync(full);
    if (st.isDirectory()) {
      collectDtsFiles(full, base, acc);
    } else if (name.endsWith('.d.ts') && name !== 'index.d.ts') {
      acc.push(full);
    }
  }
  return acc;
}

const files = collectDtsFiles(generatedRoot).sort((a, b) => a.localeCompare(b));
const lines = [
  '/** Auto-generated by scripts/write-generated-barrel.mjs — do not edit by hand. */',
  '',
];

for (const abs of files) {
  const rel = path.relative(generatedRoot, abs).replace(/\\/g, '/');
  const withoutExt = rel.replace(/\.d\.ts$/, '');
  lines.push(`export * from './${withoutExt}';`);
}

lines.push('');
const outPath = path.join(generatedRoot, 'index.d.ts');
fs.writeFileSync(outPath, lines.join('\n'), 'utf8');
console.log(`Wrote ${outPath} (${files.length} modules).`);
